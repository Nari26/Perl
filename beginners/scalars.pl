#!/usr/bin/perl 

# Numeric scalar 
$numScalar = 100;

# String Scalar 
$strScalar = "Perl";

#vString 
$vString = v85.78.73.88;

# . or , can be used to concatenating strings
# x is used for repeating the scalar
# v strings - scalar declaration using ASCII numbers

print $numScalar."-".$strScalar."-".$vString."\n";

print "=" x 10, "\n";